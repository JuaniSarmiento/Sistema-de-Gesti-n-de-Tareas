package com.utn.tareas.model;

/**
 * Enumeración que representa los niveles de prioridad de una tarea.
 * 
 * @author Sistema de Gestión de Tareas UTN
 * @version 1.0
 */
public enum Prioridad {
    
    /**
     * Prioridad alta - Tareas urgentes que requieren atención inmediata
     */
    ALTA,
    
    /**
     * Prioridad media - Tareas importantes pero no urgentes
     */
    MEDIA,
    
    /**
     * Prioridad baja - Tareas que pueden realizarse cuando haya tiempo disponible
     */
    BAJA
}
